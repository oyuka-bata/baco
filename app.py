"""## 8. Wire up the buttons

When the user clicks **Get my vibe**:

1. Read the name and the dropdown selections.
2. Convert dropdown selections into the `{question_index: option_index}` dict the recommendation pipeline expects.
3. Run `recommend(...)` to get the profile, top pick, and playlist.
4. Pick a persona, fetch real album art + audio preview, and render everything.

"""

def on_find_clicked(_):
    with output_area:
        clear_output()

        name = name_input.value.strip() or "friend"

        # Collect answers, skipping any question the user left as "—"
        answers = {}
        for i, dd in enumerate(quiz_widgets):
            if dd.value == "—":
                continue
            labels = [opt["label"] for opt in quiz[i]["options"]]
            answers[i] = labels.index(dd.value)

        if not answers:
            display(HTML(
                "<p style='color:#b00020;'>Please answer at least one question, "
                "then click <b>Get my vibe</b> again.</p>"
            ))
            return

        # 1. Recommendation pipeline
        profile, top, playlist = recommend(answers)
        persona_title, persona_desc = pick_persona(profile)

        # 2. Real cover + preview from iTunes
        meta = fetch_song_meta(top["name"], top["artist"])

        # 3. Render — persona card, top pick card, explanation, playlist, audio
        display(HTML(render_persona_card(name, persona_title, persona_desc, profile)))
        display(HTML(render_top_pick_card(top, meta)))
        display(HTML(build_explanation(top, profile)))

        if meta and meta.get("preview"):
            display(HTML("<p><b>30-second preview:</b></p>"))
            display(Audio(url=meta["preview"], autoplay=False))
        else:
            display(HTML("<p style='color:#888;'><i>(Audio preview not available for this song.)</i></p>"))

        # 4. Playlist table
        display(HTML(f"<p><b>Your BACO Station playlist ({len(playlist)} tracks):</b></p>"))
        nice_cols = ["name", "artist", "genre", "mood", "energy", "valence", "tempo", "popularity"]
        display(playlist[nice_cols].reset_index(drop=True))


def on_reset_clicked(_):
    name_input.value = ""
    for dd in quiz_widgets:
        dd.value = "—"
    with output_area:
        clear_output()


find_btn.on_click(on_find_clicked)
reset_btn.on_click(on_reset_clicked)

