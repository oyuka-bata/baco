"""## 4. Recommendation logic

Three small functions:

1. **`aggregate_profile`** — turn the user's answers into a single musical profile (target energy, target valence, genre weights, mood weights).
2. **`score_song`** — compute a match score for one song against that profile. Songs whose energy and valence are close to the target score higher; the bonus terms reward matching genre and mood.
3. **`recommend`** — score every song in the dataset, sort, and return the top pick + a small playlist.

A separate **`pick_persona`** function picks a fun, descriptive name for the user's overall vibe based on their target energy and valence.

"""

def aggregate_profile(answers):
    """Build the user's musical profile from their quiz answers.

    `answers` is a dict: { question_index: option_index }.
    """
    energy_sum = 0.0
    valence_sum = 0.0
    weight_sum = 0
    genres = {}
    moods = {}

    for qi, ai in answers.items():
        opt = quiz[qi]["options"][ai]
        w = opt["w"]
        energy_sum  += w["energy"]
        valence_sum += w["valence"]
        weight_sum  += 1
        for g, v in w["genres"].items():
            genres[g] = genres.get(g, 0) + v
        for m, v in w["moods"].items():
            moods[m] = moods.get(m, 0) + v

    return {
        "target_energy":  energy_sum  / weight_sum if weight_sum else 0.5,
        "target_valence": valence_sum / weight_sum if weight_sum else 0.5,
        "genres": genres,
        "moods":  moods,
    }


def score_song(row, profile):
    """Higher score = better match for this user's profile."""
    energy_dist  = abs(row["energy"]  - profile["target_energy"])
    valence_dist = abs(row["valence"] - profile["target_valence"])
    genre_bonus  = profile["genres"].get(row["genre"], 0) * 0.08
    mood_bonus   = profile["moods"].get(row["mood"],  0) * 0.07
    pop_bonus    = row["popularity"] / 100.0 # Changed from 1000.0 to 100.0
    return -(energy_dist + valence_dist) + genre_bonus + mood_bonus + pop_bonus


def recommend(answers, songs_df=songs, k=6):
    """Return (profile, top_pick_row, playlist_dataframe)."""
    profile = aggregate_profile(answers)
    scored  = songs_df.copy()
    scored["match_score"] = scored.apply(lambda r: score_song(r, profile), axis=1)
    scored = scored.sort_values("match_score", ascending=False)
    return profile, scored.iloc[0], scored.head(k)


