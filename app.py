"""## 5. Real album art + 30-second preview (iTunes Search API)

Apple's iTunes Search API is **free, no auth, CORS-friendly**, and returns the album cover plus a 30-second mp3 preview for most popular songs. We use it for the *top pick* so the recommendation feels like a real Spotify-style product.

Endpoint:
`https://itunes.apple.com/search?term=<song+artist>&media=music&entity=song&limit=1`

"""

def fetch_song_meta(name, artist, timeout=5):
    """Fetch real cover art URL + 30-second preview URL from iTunes.

    Returns a dict {"cover": ..., "preview": ...} or None if the song isn't found.
    """
    try:
        resp = requests.get(
            "https://itunes.apple.com/search",
            params={
                "term": f"{name} {artist}",
                "media": "music",
                "entity": "song",
                "limit": 1,
            },
            timeout=timeout,
        )
        resp.raise_for_status()
        items = resp.json().get("results", [])
        if not items:
            return None
        item = items[0]
        cover = item.get("artworkUrl100", "")
        if cover:
            cover = cover.replace("100x100bb", "600x600bb")
        return {
            "cover":   cover or None,
            "preview": item.get("previewUrl") or None,
        }
    except Exception as e:
        print("iTunes lookup failed:", e)
        return None


